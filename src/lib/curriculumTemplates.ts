/** Параллель (grade) + оқыту тілі бойынша үлгілік апталық сағаттар */
export function templateFor(grade: number, lang: SchoolLang = "kk"): Row[] {
  const g = Math.max(1, Math.min(11, grade));
  return [...(LANG_PLAN[lang][g] || []), ...(CORE_PLAN[g] || [])];
}

// Пәнді атауы/синонимі бойынша табу
function findSubject(byLower: Map<string, Subject>, name: string): Subject | undefined {
  const n = name.toLowerCase();
  const direct = byLower.get(n);
  if (direct) return direct;
  // синонимдер: үлгі атауының баламасы мектепте бар ма
  const aliases = SUBJECT_ALIASES[n] || [];
  for (const a of aliases) { const s = byLower.get(a); if (s) return s; }
  // кері бағыт: мектеп пәні үлгі атауының синонимі ме (жартылай сәйкестік)
  for (const [key, alist] of Object.entries(SUBJECT_ALIASES))
    if (key === n || alist.includes(n))
      for (const cand of [key, ...alist]) { const s = byLower.get(cand); if (s) return s; }
  return undefined;
}

/** Үлгіден оқу жоспарын құру (500-бұйрық + оқыту тілі). Пайдаланушының пән
    тізімінен атауы/синонимі бойынша табады; табылмағандар тізіммен қайтады. */
export function buildFromTemplate(cls: Klass, subjects: Subject[], lang: SchoolLang = "kk"): { items: CurItem[]; missing: string[] } {
  const byLower = new Map(subjects.map((s) => [s.name.toLowerCase(), s]));
  const items: CurItem[] = [];
  const missing: string[] = [];
  const seen = new Set<string>();
  for (const [name, hours] of templateFor(cls.grade, lang)) {
    const s = findSubject(byLower, name);
    if (s && !seen.has(s.id)) { items.push({ id: uid(), subjectId: s.id, hours }); seen.add(s.id); }
    else if (!s) missing.push(name);
  }
  return { items, missing };
}

/** Мұғалімі жоқ жолдарға (жеке + топтар) ЕҢ БОС мұғалімді автотағайындау.
    Бюджет тағайындау барысында азайтылып отырады — бір мұғалімге бәрін
    үйіп тастамайды. Өзгертілген жолдар санын қайтарады. */
export function autoAssignTeachers(
  cls: Klass, curriculum: CurItem[], teachers: Teacher[], allClasses: Klass[], subjects?: Subject[]
): { items: CurItem[]; assigned: number; unassigned: string[] } {
  // ағымдағы бюджет (осы сыныптың толтырылмағандары әлі есепте жоқ)
  const budgets = teacherBudgets(teachers, allClasses);
  const nameOf = new Map((subjects || []).map((s) => [s.id, s.name]));
  // Мұғалім осы пәнді бере ала ма (декларацияланған пән тізімі бойынша;
  // тізімі бос — шектеусіз). Атау белгісіз болса — шектемейміз.
  const canTeach = (t: Teacher, subjectId: string) => {
    if (!t.subjects?.length) return true;
    const nm = nameOf.get(subjectId);
    return !nm || t.subjects.includes(nm);
  };
  const eligible = (need: number, subjectId: string, exclude?: Set<string>): TeacherBudget | null => {
    const list = [...budgets.values()]
      .filter((b) =>
        b.teacher.gradeMin <= cls.grade && cls.grade <= b.teacher.gradeMax &&
        (b.teacher.shift === 3 || b.teacher.shift === cls.shift) &&
        canTeach(b.teacher, subjectId) &&   // пәнін бермейтін мұғалім тағайындалмайды
        (!exclude || !exclude.has(b.teacher.id)))
      .sort((a, b) => b.free - a.free);
    // ПӘН БІЛІКТІЛІГІ басым: осы пәнді басқа жерде беретін мұғалім алдымен
    const match = list.filter((b) => b.subjects.has(subjectId));
    const fit = match.find((b) => b.free >= need) || list.find((b) => b.free >= need);
    return fit || null; // сыймаса — тағайындамаймыз (жасырын асып кетпес үшін)
  };
  let assigned = 0;
  const unassigned: string[] = [];
  const items = curriculum.map((cu) => {
    if (cu.isSplit) {
      const used = new Set((cu.groups || []).map((g) => g.teacherId).filter(Boolean));
      const groups = (cu.groups || []).map((g) => {
        if (g.teacherId) return g;
        const b = eligible(cu.hours, cu.subjectId, used);
        if (!b) { unassigned.push(cu.subjectId); return g; }
        b.free -= cu.hours; b.assigned += cu.hours; used.add(b.teacher.id); assigned++;
        return { ...g, teacherId: b.teacher.id };
      });
      return { ...cu, groups };
    }
    if (cu.teacherId) return cu;
    const b = eligible(cu.hours, cu.subjectId);
    if (!b) { unassigned.push(cu.subjectId); return cu; }
    b.free -= cu.hours; b.assigned += cu.hours; assigned++;
    return { ...cu, teacherId: b.teacher.id };
  });
  return { items, assigned, unassigned };
}
